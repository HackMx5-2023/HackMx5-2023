    <h3>Creando soluciones contra el Phishing</h3>
    <p class="text-white"><strong>Empresa: </strong>Banorte</p>
      <p class="text-white"><strong>Dificultad: </strong> MEDIA-ALTA</p>
      <p class="text-white"><strong>Objetivo: </strong>Utilizar la tecnología para evitar el impacto del Phishing hacia los usuarios finales y prevenir el robo o fraude digital</p>
      <p class="text-white"><strong>Tecnologías: </strong> A consideración del equipo.</p>
      <p class="text-white"><strong>Elementos indispensables: </strong> Demo/Prototipo</p>
      <p class="text-white"><strong>Descripción: </strong> Actualmente las Instituciones Financieras se encuentran en constante ataque por parte de Ciber Delincuentes que generan sitios faltos (Phishing) con el objetivo de utilizarlos para el robo de información a los clientes.El contar con una capacidad ágil para su identificación, y baja por parte de los Bancos, es crucial para evitar el impacto hacia los usuarios finales y prevenir el robo o fraude digital.
</p>
       <p class="text-white">El phishing es una técnica de ingeniería social utilizada por los
         delincuentes para obtener información confidencial como nombres de usuario, contraseñas y detalles
          de tarjetas de crédito haciéndose pasar por una entidad confiable y legítima. <br/>
          El escenario de Phishing generalmente está asociado con la capacidad de duplicar una página web para
          hacer creer al visitante que se encuentra en el sitio web legítimo. El engaño suele llevarse a cabo a través
          de correo electrónico y, a menudo estos correos contienen enlaces a un sitio web falso con una apariencia
          casi idéntica a un sitio legítimo. A través del sitio los usuarios incautos son engañados para que ingresen
          datos confidenciales, lo que le proporciona a los delincuentes un amplio margen para realizar estafas y fraudes
          con la información obtenida.<br/>
          La principal manera de llevar adelante el engaño es a través del envío de spam (correo no deseado) e invitando al
          usuario a acceder a la página señuelo. El objetivo del engaño es adquirir información confidencial del usuario como
          contraseñas, tarjetas de crédito o datos financieros y bancarios. <br/>
          Algunas de las características más comunes que presentan este tipo de mensajes de correo electrónico son:<br/>

      </p>
      <ul class="text-white">
    <li>Uso de nombres de compañías existentes. En lugar de crear desde cero el sitio web de una compañía ficticia, los emisores de correos con intenciones fraudulentas adoptan la imagen corporativa y funcionalidad del sitio de web de una empresa existente, con el fin de confundir aún más al receptor del mensaje.</li>
    <li>Utilizar el nombre de un empleado real de una empresa conocida como remitente del correo falso. De esta manera, si el receptor intenta confirmar la veracidad del correo llamando a la compañía, desde ésta le podrán confirmar que la persona que dice hablar en nombre de la empresa trabaja en la misma.</li>
    <li>Direcciones web con la apariencia correcta. Como hemos visto, el correo fraudulento suele conducir al lector hacia sitios web que replican el aspecto de la empresa que está siendo utilizada para robar la información. En realidad, tanto los contenidos como la dirección web (URL) son falsos y se limitan a imitar los contenidos reales. Incluso la información legal y otros enlaces no vitales pueden redirigir al confiado usuario a la página web real.</li>
    <li>Factor miedo. La ventana de oportunidad de los defraudadores es muy breve, ya que una vez se informa a la compañía de que sus clientes están siendo objeto de este tipo de prácticas, el servidor que aloja al sitio web fraudulento y sirve para la recogida de información se cierra en el intervalo de unos pocos días, por lo tanto, es fundamental para el defraudador el conseguir una respuesta inmediata por parte del usuario. En muchos casos, el mejor incentivo es amenazar con una pérdida, ya sea económica o de la propia cuenta existente, si no se siguen las instrucciones indicadas en el correo recibido, y que usualmente están relacionadas con nuevas medidas de seguridad recomendadas por la entidad.</li>
      </ul>
      <p class="text-white">Generación de una propuesta tecnológica en donde a través del uso de nuevas tecnologías como Machine Learning, DeepLearning, Inteligencia Artificial, BlockChain, User Behavior Analysis, etc. se proponga un diseño de solución que atienda la problemática para identificación y prevención de Phishing.<br/>
        Los participantes deberán generar un prototipo en el que se demuestre la idea principal de su diseño y deberán acompañarlo con una presentación en la que expliquen las características de la propuesta y su modelo de operación.
      </p>
