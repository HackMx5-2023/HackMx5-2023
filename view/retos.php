    <p class="text-white"><strong>Empresa: </strong> Konfio</p>
      <p class="text-white"><strong>Dificultad: </strong> Media</p>
    <p class="text-white"><strong>Objetivo: </strong>Realizar una API la cual genere “préstamos al instante”. Esto es la forma en la que KONFIO se desarrolla con la automatización de procesos. AWS es clave en el crecimiento de los estudiantes dado que Cloud Computing es una habilidad que rápidamente se ha vuelto un gran diferenciador en el mercado tanto para empresas como startups, al punto que el sistema educativo continúa adaptándose a su rápida irrupción, con esto empezaran a  conocer y a familiarizarse con este tipo de herramientas.</p>
      <p class="text-white"><strong>Tecnologías: </strong>AWS</p>
      <p class="text-white"><strong>Elementos indispensables: </strong>Una página web donde se puedan insertar los datos de los clientes.<br/>
        Una respuesta de la API con la propuesta económica con las cifras y clasificaciones esperadas.
        <br/>La propuesta económica en la página web.</p>
    <p class="text-white"><strong>Descripción: </strong> Konfio recibe miles de propuestas de préstamos al día y quiere que construyas una API con un Cloud Provider, en la cual recibas los datos del cliente, con los cuales se clasifique al cliente y se le entregue una propuesta económica y todo esto lo muestres en una página web.<br/>
    Konfio puede generar el resultado de una petición de préstamo en menos de 4 minutos, esto es gracias a un conjunto de APIs, Scraping y otras herramientas tecnológicas. Estas herramientas analizan los datos del cliente, obtienen valores y regresan al cliente con una calificación con  la cual se le ofrece una propuesta económica.<br/>
    Utilizar Cloud Computing es esencial para automatización de procesos en las empresas hoy en día, al poder involucrar conceptos novedosos como procesamientos Serverless, almacenamiento en data lakes, y procesamiento en bases relacionales y Key-Pair, y el estar familiarizado con estas tecnologías te da un gran crecimiento en tu carrera profesional y experiencia.<br/>
